                                    The Board’s role is to oversee and manage the Group as responsibly and efficiently as possible. It meets formally at least six times a year, to discuss a broad range of significant matters including vision, strategy, operational performance, funding and financial performance. All significant decisions are made by the Directors with all Directors participating in the key areas of decision-making, including the appointment of new Directors. The Board receives timely information on all material aspects for the Group to enable it to discharge its duties. The CEO provides an appraisal of the current status and short-term plans for operational and development activities. In addition to its fixed meeting schedule the Board convenes restricted agenda meetings on an ad hoc basis. Minutes of the meetings of the Directors are also circulated to the Board for approval.<br>
