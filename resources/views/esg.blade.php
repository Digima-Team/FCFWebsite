                              <p>The Company's governance structures are further governed by the Company's Articles of Association (“Articles”) together with Relationship Agreements and a Revolving Credit Facility (“RCF”) entered into in October 2020 with the Company's two largest shareholders, MTL (Luxembourg) S.à r.l. (“MTL Lux”) and Runruno Holdings Limited (“RHL”), as part of the Group debt restructuring.</p>
                              <p>The Relationship Agreements regulate the relationship between the Company and its two largest shareholders to ensure, amongst other things, that the Group and its business shall be managed for the benefit of the shareholders of the Company as a whole. The relationship agreements grant each shareholder the right to appoint one director, for so long as it (together with its successors or assigns) continues to hold more than 10% of the voting rights of the Company.</p>
